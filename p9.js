// Generating a random number between 0 (inclusive) and 1 (exclusive)
let randomNumber = Math.random();
console.log("Random number: " + randomNumber);

// Finding the largest number between 10 and 20
let largestNumber = Math.max(10, 20);
console.log("Largest number: " + largestNumber);

// Finding the square root of 9
let squareRoot = Math.sqrt(9);
console.log("Square root of 9: " + squareRoot);
